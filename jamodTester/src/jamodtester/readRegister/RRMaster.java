    private class refresh extends SwingWorker<Object, Object>
    {
        
        public refresh() {
        }

        @Override
        protected Object doInBackground() throws Exception {
            int port = Modbus.DEFAULT_PORT;
            InetAddress addy = InetAddress.getLocalHost();
            TCPMasterConnection connection = new TCPMasterConnection(addy);
            connection.setTimeout(3000);
            connection.setPort(port);
            System.out.println("Trying to connect to "+addy.getHostAddress()+" on port "+port);
            connection.connect();
            
            ModbusTCPTransaction transaction = new ModbusTCPTransaction(connection);
            
            ModbusRequest request = new ReadMultipleRegistersRequest(0,1);
            request.setUnitID(15);
            transaction.setRequest(request);
            transaction.execute();
            ModbusResponse response = transaction.getResponse();
            System.out.println(response.getHexMessage());
            String hexString = response.getHexMessage().substring(response.getHexMessage().length()-3, response.getHexMessage().length()-1);
            int regVal = Integer.parseInt(hexString, 16);
            jLabel1.setText("Register 0: " + regVal);
            connection.close();
            
            return 0;
        }
    }
    
